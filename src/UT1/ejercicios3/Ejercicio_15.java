package UT1.ejercicios3;

/*
Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su correspondiente traducción al castellano.
Las palabras deben estar distribuidas en dos columnas y alineadas a la izquierda. Pista: Se puede insertar un tabulador mediante \t.
*/

public class Ejercicio_15 {
    public static void main(String[] args) {
        System.out.println("grey\tgris");
        System.out.println("pencil\tlapiz");
        System.out.println("bottle\tbotella");
        System.out.println("number\tnumero");
        System.out.println("green\tverde");
        System.out.println("laptop\tportatil");
        System.out.println("phone\ttelefono");
        System.out.println("mouse\traton");
        System.out.println("paper\tpapel");
        System.out.println("book\tlibro");
    }
}
